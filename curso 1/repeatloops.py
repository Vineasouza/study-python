#comando while
x = 0

while x < 10:
    print(x)
    x += 1

#comando for
lista1 = [1,2,3,4,5]
lista2 = ["hello","world","!"]

for i in lista1:
    print(i)

for i in lista2:
    print(i)

#comando for...range
for i in range(10):
    print(i)

for i in range(10,20):
    print(i)

for i in range(10,20,2):
    print(i)