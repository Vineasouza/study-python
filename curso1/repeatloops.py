"""
Using while loop
"""

X_VAR = 0

while X_VAR < 10:
    print(X_VAR)
    X_VAR += 1

#comando for
LISTA_1 = [1,2,3,4,5]
LISTA_2 = ["hello","world","!"]

for i in LISTA_1:
    print(i)

for i in LISTA_2:
    print(i)

#comando for...range
for i in range(10):
    print(i)

for i in range(10,20):
    print(i)

for i in range(10,20,2):
    print(i)
    