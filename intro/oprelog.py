#operadores relacionais
x = 2
y = 3

print("IGUAL:")
print(x == y)
print("MAIOR IGUAL:")
print(x >= y)
print("MAIOR:")
print(x > y)
print("MENOR IGUAL:")
print(x <= y)
print("MENOR:")
print(x < y)


#operadores logicos
x = 2
y = 2
z = 3

print("AND:")
print(x == y and x == z)
print("OR:")
print(x == y or x == z)


